 public void fetchPropertiesForRent() {
        DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
        
        // Clear existing data in the JTable
        model.setRowCount(0);

        try {
            // SQL query to fetch properties where 'Available' is 'rent'
            String query = "SELECT OwnerName, PropertyName, ContactNo, HousingConfiguration, Landmark, Address, Description, Amenities,Price FROM Properties WHERE Available = 'rent'";
            PreparedStatement pstmt = conn.prepareStatement(query);
            ResultSet rs = pstmt.executeQuery();

            // If no data is found, inform the user and exit early
            if (!rs.isBeforeFirst()) {
                JOptionPane.showMessageDialog(null, "No properties available for rent.");
                return;  // Exit if no rows in the result set
            }

            // Iterate over the ResultSet and populate the DefaultTableModel
            while (rs.next()) {
                // Fetch each value from the result set
                String ownerName = rs.getString("OwnerName");
                String propertyName = rs.getString("PropertyName");
                String contactNo = rs.getString("ContactNo");
                String housingConfig = rs.getString("HousingConfiguration");
                String landmark = rs.getString("Landmark");
                String address = rs.getString("Address");
                String description = rs.getString("Description");
                String amenities = rs.getString("Amenities");
                String price = rs.getString("Price");

                // Add a new row to the table model
                Object[] row = {
                    ownerName,
                    propertyName,
                    contactNo,
                    housingConfig,
                    landmark,
                    address,
                    description,
                    amenities,
                    price
                };

                model.addRow(row);  // Add the row to the JTable
            }

            // Close resources
            rs.close();
            pstmt.close();

        } catch (SQLException e) {
            // Handle SQL exceptions and provide feedback to the user
            JOptionPane.showMessageDialog(null, "Error fetching properties for rent: " + e.getMessage());
        }
    }
