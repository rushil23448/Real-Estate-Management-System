    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
       if (evt.getButton() == MouseEvent.BUTTON1) {  // Check for left-click
        // First confirmation dialog
        int initialResponse = JOptionPane.showConfirmDialog(
            null,
            "Are you sure you want to buy this property?",
            "Confirm Purchase",
            JOptionPane.YES_NO_OPTION
        );

        if (initialResponse == JOptionPane.YES_OPTION) {
            // User confirmed the initial intent to buy

            // Second confirmation dialog
            int finalResponse = JOptionPane.showConfirmDialog(
                null,
                "This action will finalize the purchase. Are you sure?",
                "Final Confirmation",
                JOptionPane.YES_NO_OPTION
            );

            if (finalResponse == JOptionPane.YES_OPTION) {
                // User confirmed the final prompt
                System.out.println("User confirmed the final purchase.");

                // Display congratulatory message
                JOptionPane.showMessageDialog(
                    null,
                    "Congratulations!! Property bought.",
                    "Purchase Success",
                    JOptionPane.INFORMATION_MESSAGE
                );

            } else {
                // User declined the final confirmation
                System.out.println("User declined the final confirmation.");
            }
        } else {
            // User declined the first prompt
            System.out.println("User declined the initial purchase intent.");
        }
    }
    }//GEN-LAST:event_jTable2MouseClicked

